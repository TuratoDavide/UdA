package myapp.model;

public class ElementoTeam {
    String username;
    String ruolo;

    public ElementoTeam(String username, String ruolo) {
        this.username = username;
        this.ruolo = ruolo;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getRuolo() {
        return ruolo;
    }

    public void setRuolo(String ruolo) {
        this.ruolo = ruolo;
    }
    
}
